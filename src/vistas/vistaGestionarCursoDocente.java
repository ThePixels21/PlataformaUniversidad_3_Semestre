    public void crearTablaEstudiantes(){
        DefaultTableModel model = new DefaultTableModel();
        String[] columnas = new String[]{
            "Documento", "Nombre","Apellido", "N1","N2", "N3", "Acumulado"
        };
        model.setColumnIdentifiers(columnas);
        for (int i = 0; i < curso.getNotas().getSize(); i++) {
            Estudiante estudiante = curso.getNotas().get(i).getEstudiante();
            model.addRow(new Object[]{
                estudiante.getDocumento(),
                estudiante.getNombre(),
                estudiante.getApellido(),
                Double.toString(curso.getNotas().get(i).getNota1()),
                Double.toString(curso.getNotas().get(i).getNota2()),
                Double.toString(curso.getNotas().get(i).getNota3()),
                String.format("%.3f", curso.getNotas().get(i).getAcumulado()),
            });
        }
        tableEstudiantes.setModel(model);
    }
    
    private void crearLabels(){
        lblAsignatura.setText("Asignatura: " + curso.getAsignatura().getNombre());
        lblDocente.setText("Docente: " + curso.getDocente().getNombre() + " " + curso.getDocente().getApellido());
        lblDia.setText("Día: " + curso.getDia());
        lblHora.setText("Hora: " + consultarHrsCurso(curso));
    }
    
    private String consultarHrsCurso(Curso curso){
        String hrs = "";
        for (int i = 0; i < curso.getHoras().length; i++) {
            hrs += curso.getHoras()[i];
            if(i < curso.getHoras().length-1){
                hrs += ", ";
            }
        }
        return hrs;
    }
    
    private void crearComboBox(){
        cboxSalon.setModel(new DefaultComboBoxModel(vistaAnterior.getControlador().crearArregloSalonesDisponibles(curso)));
    }
