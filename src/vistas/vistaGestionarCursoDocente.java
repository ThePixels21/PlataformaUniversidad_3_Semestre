    private void cboxSalonItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboxSalonItemStateChanged
        // TODO add your handling code here:
        if(Integer.parseInt(cboxSalon.getSelectedItem().toString()) != curso.getSalon().getNumero()){
            Salon salon = vistaAnterior.getControlador().buscarSalon(Integer.parseInt(cboxSalon.getSelectedItem().toString()));
            if(salon != null){
                curso.setSalon(salon);
                JOptionPane.showMessageDialog(this, "El salón del curso ha sido modificado");
            }else{
                JOptionPane.showMessageDialog(this, "No se pudo modificar el salón");
            }
            crearComboBox();
        }
    }//GEN-LAST:event_cboxSalonItemStateChanged

